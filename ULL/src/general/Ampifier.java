package general;

public class Ampifier {

	private double averageAmpifierKm=0;
	private double AmpifierKm=0;
	private double AmpifierGain=0;
	private double NF=0;
	private double Distance=0;
	
	
	public double getAverageAmpifierKm() {
		return averageAmpifierKm;
	}
	public void setAverageAmpifierKm(double averageAmpifierKm) {
		this.averageAmpifierKm = averageAmpifierKm;
	}
	public double getAmpifierKm() {
		return AmpifierKm;
	}
	public void setAmpifierKm(double ampifierKm) {
		AmpifierKm = ampifierKm;
	}
	public double getAmpifierGain() {
		return AmpifierGain;
	}
	public void setAmpifierGain(double ampifierGain) {
		AmpifierGain = ampifierGain;
	}
	public double getNF() {
		return NF;
	}
	public void setNF(double nF) {
		NF = nF;
	}
	public double getDistance() {
		return Distance;
	}
	public void setDistance(double distance) {
		Distance = distance;
	}
	
	
}
